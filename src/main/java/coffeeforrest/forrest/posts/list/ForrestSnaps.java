package coffeeforrest.forrest.posts.list;

import coffeeforrest.forrest.ForrestObject;

public class ForrestSnaps extends ForrestObject {
	
	private static final long serialVersionUID = 3312699581871000976L;
	
	// fields
	private String mega_url;
	private String keith_url;
	private String large_url;
	private String medium_url;
	private String small_url;
	private String thumb_url;
	private String original_url;
	
	
	/**
	 * @return the mega_url
	 */
	public String getMegaUrl() {
		return mega_url;
	}
	
	/**
	 * @return the keith_url
	 */
	public String getKeithUrl() {
		return keith_url;
	}
	
	/**
	 * @return the large_url
	 */
	public String getLargeUrl() {
		return large_url;
	}
	
	/**
	 * @return the medium_url
	 */
	public String getMediumUrl() {
		return medium_url;
	}
	
	/**
	 * @return the small_url
	 */
	public String getSmallUrl() {
		return small_url;
	}
	
	/**
	 * @return the thumb_url
	 */
	public String getThumbUrl() {
		return thumb_url;
	}
	
	/**
	 * @return the original_url
	 */
	public String getOriginalUrl() {
		return original_url;
	}

}
